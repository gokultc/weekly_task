# OOPS - object oriented programming language

# CLASS     : Blueprint or base model

# OBJECT    : using the blueprint (class) we build the model

# REFERENCE : operations or activities done in the object

# example
# Tv - class
# samsung,Lg,redmi,oneplus.... - object
# changing channel,volume up,... - reference

# phone - class
# sam,iphn,oneplus,vivo,.. - object
# call,chat,camera,..- reference


