# for loop
#for loops are used to do same condition multiple times

#           (start,stop)
for i in range(1,10): #works till 9
    print("hello")

#if we run the code it won't execute till the last value

#it is (last_num-1)

#the default increment value of the for loop is 1

#floting point is not possible in integer


#INCREMENT
#            (start,stop,step)
