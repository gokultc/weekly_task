n1=int(input("Enter the 1st number :"))
n2=int(input("Enter the 2st number :"))
oper=input("Enter the operation to perform :")
if oper=='+':
    print("sum=",n1+n2)
elif oper=='-':
    print("sub=",n1-n2)
elif oper=='*':
    print("multi=", n1 * n2)
elif oper=='/':
    print("division=", n1/n2)
elif oper=='%':
    print("modulus=", n1%n2)



